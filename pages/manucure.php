let scrollPercentage = () => {
    let pos = document.documentElement.scrollTop;
    let calcHeight = document.documentElement.scrollHeight - document.documentElement.clientHeight;
    let scrollValue = Math.round(pos * 100 / calcHeight);

    if (scrollValue >= 0 && scrollValue <= 23) {
        document.getElementById("secondCercle2").classList.remove("cerclepurple");
    } else if (scrollValue >= 23 && scrollValue <= 65) {
        document.getElementById("secondCercle2").classList.add("cerclepurple");
    } else {
        document.getElementById("secondCercle2").classList.remove("cerclepurple");
    }
}

// Animation image accueil

window.onscroll = scrollPercentage;

$(window).scroll(function() {
    var scrollPos = $(this).scrollTop() / 4;
    $('.imageMid').css({
        'left': 50 + scrollPos + '%',
        'top': 50 - scrollPos + '%'
    })

    var scrollPos1 = $(this).scrollTop() / 10;
    $('.imageRight').css({
        'left': 10 - scrollPos1 + '%',
        'top': 40 - scrollPos1 + '%'
    })
    $('.imageLeftt').css({
        'left': 60 + scrollPos1 + '%',
